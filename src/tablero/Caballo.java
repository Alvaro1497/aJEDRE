package tablero;
public class Caballo extends Pieza {

	private String caballoBlanca;
	private String caballoNegro;
	public String getCaballoBlanca() {
		return caballoBlanca;
	}
	public void setCaballoBlanca(String caballoBlanca) {
		this.caballoBlanca = caballoBlanca;
	}
	public String getCaballoNegro() {
		return caballoNegro;
	}
	public void setCaballoNegro(String caballoNegro) {
		this.caballoNegro = caballoNegro;
	}

	
}
